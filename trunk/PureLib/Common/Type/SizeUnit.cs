using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace PureLib.Common {
    /// <summary>
    /// Specifies the size unit.
    /// </summary>
    public enum SizeUnit {
        /// <summary>
        /// Indicates byte.
        /// </summary>
        Byte = 0,
        /// <summary>
        /// Indicates kilobyte.
        /// </summary>
        Kilobyte = 1,
        /// <summary>
        /// Indicates megabyte.
        /// </summary>
        Megabyte = 2,
        /// <summary>
        /// Indicates gigabyte.
        /// </summary>
        Gigabyte = 3,
        /// <summary>
        /// Indicates terabyte.
        /// </summary>
        Terabyte = 4,
        /// <summary>
        /// Indicates petabyte.
        /// </summary>
        Petabyte = 5,
        /// <summary>
        /// Indicates exabyte.
        /// </summary>
        Exabyte = 6,
        /// <summary>
        /// Indicates zettabyte.
        /// </summary>
        Zettabyte = 7,
        /// <summary>
        /// Indicates yottabyte.
        /// </summary>
        Yottabyte = 8
    }
}
